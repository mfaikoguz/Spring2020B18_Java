package day02_Variables;

public class EscapeSequances {
    public static void main(String[] args) {
        System.out.println("\tHello"); //tab space
        System.out.println("Hello \nWorld"); //new line
        System.out.println("My favorite TV show is \"Game of Thrones\""); // double quotes
        System.out.println("\\"); //prints \
        System.out.println("\'"); // prints '
    }
}
