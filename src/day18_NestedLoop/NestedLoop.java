package day18_NestedLoop;

public class NestedLoop {
    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {

            for (int i = 1; i <= 5; i++) {
                System.out.print(i + ",");
            }
        }
//                          this loop prints 1,2,3,4,5, 10 times

    }
}
